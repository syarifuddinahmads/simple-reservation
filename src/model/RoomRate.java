package model;

/**
 *
 * @author syarifuddin
 */
public class RoomRate {
    private int idRoomRate;
    private String nameRoomRate;
    private Double priceRoomRate;

    public int getIdRoomRate() {
        return idRoomRate;
    }

    public void setIdRoomRate(int idRoomRate) {
        this.idRoomRate = idRoomRate;
    }

    public String getNameRoomRate() {
        return nameRoomRate;
    }

    public void setNameRoomRate(String nameRoomRate) {
        this.nameRoomRate = nameRoomRate;
    }

    public Double getPriceRoomRate() {
        return priceRoomRate;
    }

    public void setPriceRoomRate(Double priceRoomRate) {
        this.priceRoomRate = priceRoomRate;
    }
    
    
}
