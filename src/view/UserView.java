    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            namaUser = tfNama.getText();
            emailUser = tfEmail.getText();
            nikUser = tfNik.getText();
            alamatUser = taAlamat.getText();
            username = tfUsername.getText();
            password = tfPassword.getText();

            Users u = new Users();
            u.setIdUser(idUser);
            u.setNameUser(namaUser);
            u.setEmailUser(emailUser);
            u.setAddressUser(alamatUser);
            u.setNikUser(Integer.parseInt(nikUser));
            u.setRoleUser(roleUser);
            u.setUsername(username);
            u.setPassword(password);
            this.userController.insertDataUser(u);

            this.getDataUser();

            tfNama.setText("");
            tfEmail.setText("");
            tfNik.setText("");
            taAlamat.setText("");
            tfPassword.setText("");
            tfUsername.setText("");
            cbRole.setSelectedIndex(0);
            btnSimpan.setText("Simpan");
            idUser = 0;

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

