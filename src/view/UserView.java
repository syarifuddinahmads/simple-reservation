    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int col = 0;
        int row = tblUser.getSelectedRow();
        System.out.println("ROW = " + row);
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "User belum dipilih, Pilih user terlebih dahulu !");
        } else {
            String id = tblUser.getModel().getValueAt(row, col).toString();
            System.out.println("ID = " + id);
            try {
                Users uid = new Users();
                uid.setIdUser(Integer.parseInt(id));
                Users us = this.userController.getDataUsers(uid).get(0);

                idUser = us.getIdUser();
                tfNama.setText(us.getNameUser());
                tfEmail.setText(us.getEmailUser());
                tfNik.setText(String.valueOf(us.getNikUser()));
                taAlamat.setText(us.getAddressUser());
                tfUsername.setText(us.getUsername());
                cbRole.getModel().setSelectedItem(us.getUserRole().getIdRole()+" -"+us.getUserRole().getNameRole());

                btnSimpan.setText("Update");
            } catch (SQLException ex) {
                Logger.getLogger(UserView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int col = 0;
        int row = tblUser.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "User belum dipilih, Pilih customer terlebih dahulu !");
        } else {

            try {
                String id = tblUser.getModel().getValueAt(row, col).toString();
                System.out.println("ID = " + id);
                this.userController.deleteDataUser(Integer.parseInt(id));
                this.getDataUser();
            } catch (SQLException ex) {
                Logger.getLogger(UserView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void getDataUser() throws SQLException {
        DefaultTableModel dtmUser = new DefaultTableModel(new String[]{"ID", "NIK", "Nama", "Email", "Username", "Role", "Alamat"}, 0);
        dtmUser.setRowCount(0);
        for (Users u : this.userController.getDataUsers(null)) {
            dtmUser.addRow(new String[]{String.valueOf(u.getIdUser()), String.valueOf(u.getNikUser()), u.getNameUser(), u.getEmailUser(), u.getUsername(), u.getUserRole().getNameRole(), u.getAddressUser()});
        }
        tblUser.setModel(dtmUser);

    }

    public void getDataRole() throws SQLException {
        DefaultComboBoxModel dcbru = new DefaultComboBoxModel();
        dcbru.addElement("0 - Pilih Role ");
        for (RoleUser ru : this.userController.getDataRole(null)) {
            dcbru.addElement(ru.getIdRole() + " - " + ru.getNameRole());
        }
        this.cbRole.removeAllItems();
        this.cbRole.setModel(dcbru);

        this.cbRole.addItemListener((ItemEvent e) -> {
            String item = cbRole.getSelectedItem().toString();
            int id = Integer.parseInt(item.substring(0, item.indexOf(" -")));
            if (id > 0) {
                roleUser = id;
            } else {
                roleUser = 0;
            }
        });
    }

